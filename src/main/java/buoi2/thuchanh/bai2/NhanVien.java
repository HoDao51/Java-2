public class NhanVien {
    private String maNV;
    private String hoTen;
    private int tuoi;
    private String diaChi;
    private String gioiTinh;

    public NhanVien(String maNV, String hoTen, int tuoi, String diaChi, String gioiTinh) {
        this.maNV = maNV;
        this.hoTen = hoTen;
        this.tuoi = tuoi;
        this.diaChi = diaChi;
        this.gioiTinh = gioiTinh;
    }
}
